/**
 * Comète product logo.
 *
 * Renders the Comète icon (drop shape) or the full logo (icon + wordmark)
 * in brand, neutral, or inverse appearance.
 */
export function Comete({
  appearance = "brand",
  type = "logo",
  size = 32,
  className,
  ...svgProps
}: LogoProps) {
  const gradientId = `comete-grad-${appearance}`;

  // --- Icon only -----------------------------------------------------------
  if (type === "icon") {
    const useGradient = appearance !== "neutral";
    return (
      <svg
        xmlns="http://www.w3.org/2000/svg"
        viewBox={ICON_VIEWBOX}
        width={size}
        height={size}
        fill="none"
        aria-hidden="true"
        className={className}
        {...svgProps}
      >
        <path
          d={ICON_PATH}
          fill={useGradient ? `url(#${gradientId})` : "#1E3661"}
        />
        {useGradient && (
          <CometeGradient
            id={gradientId}
            transform="translate(10.0825 10.3942) scale(44.2454 45.1256)"
          />
        )}
      </svg>
    );
  }

  // --- Full logo (icon + wordmark) -----------------------------------------
  const data =
    appearance === "brand"
      ? WORDMARK_BRAND
      : appearance === "inverse"
        ? WORDMARK_INVERSE
        : WORDMARK_NEUTRAL;

  const useGradient = appearance !== "neutral";
  // Compute width from viewBox aspect ratio
  const [, , vbW, vbH] = data.viewBox.split(" ").map(Number);
  const width = size * ((vbW ?? 1) / (vbH ?? 1));

  return (
    <svg
      xmlns="http://www.w3.org/2000/svg"
      viewBox={data.viewBox}
      width={width}
      height={size}
      fill="none"
      aria-hidden="true"
      className={className}
      {...svgProps}
    >
      {/* Wordmark text paths */}
      {data.textPaths.map((d, i) => (
        <path key={i} d={d} fill={data.textFill} />
      ))}
      {/* Integrated icon (replaces the "o" in comète) */}
      <path
        d={data.iconPath}
        fill={
          useGradient
            ? `url(#${gradientId})`
            : (data as typeof WORDMARK_NEUTRAL).iconFill ?? data.textFill
        }
      />
      {useGradient && (
        <CometeGradient
          id={gradientId}
          transform={
            appearance === "brand"
              ? WORDMARK_BRAND.iconGradientTransform
              : "translate(26.0825 10.3942) scale(44.2454 45.1256)"
          }
        />
      )}
    </svg>
  );
}

Comete.displayName = "Comete";
